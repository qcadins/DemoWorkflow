public class CountRowAssetAttribute {
	@Keyword
	public countRowAssetAttribute (Sql instance, String assetcode){
		String countRowAssetAttributeResult
		instance.eachRow(("select count(REF_ATTR_ID) from ASSET_MASTER a join ASSET_ATTR b on a.ASSET_TYPE_ID = b.ASSET_TYPE_ID where FULL_ASSET_CODE = '" + assetcode + "'"), {  row ->

			countRowAssetAttributeResult = (row[0])
		})
		return countRowAssetAttributeResult
	}
